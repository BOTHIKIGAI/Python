""" ¿Que es una variable Float? """

""" 
Es un tipo de dato que nos permite representar un número positivo o negativo
con decimales, es decir, números reales.
Son utiles para almacenar números que requieren mucha precisión.
Los números flotantes se asignan a las variables mediante un solo igual (=) y estos contienen un punto decimal.

"""

a = 1.0;
b = 3 / 1;
c = 3.15;

print("a: ", a);
print("b: ", b);
print("c: ", c);