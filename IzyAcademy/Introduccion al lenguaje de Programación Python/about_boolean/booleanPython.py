""" ¿Qué es una variable tipo Boolean? """

""" 
Una variable boolean es una variable que sólo puede tomar dos posibilidades: True o False
En python cualquier variable (en general, cualquier objeto) puede considerarse como una
variable boolean. En general los elementos nulos o vacíos se consideran False y el resto
se considera True.
"""

print(10 > 9);
print(10 == 9);
print(10 < 9);

estado_1 = True;
estado_2 = False;
print(estado_1);
print(estado_2);